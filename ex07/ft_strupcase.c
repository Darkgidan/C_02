char	*ft_strupcase(char *str)
{
	char *ptr;

	ptr = str;
	if (*str == '\0')
		return (str);
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
			*str -= 32;
		str++;
	}
	return (ptr);
}
